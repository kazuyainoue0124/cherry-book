# クラス
## クラスはデータ型の一種
## Rubyでは全てのオブジェクトは何らかのクラスに属している
## クラスが同じであれば保持している属性やメソッドは同じ

# オブジェクト、インスタンス、レシーバ
## オブジェクトはクラスをもとにして作られたデータのかたまり
## オブジェクトはインスタンスやレシーバと呼ばれることもある

# メソッド、メッセージ
## オブジェクトが持つ「動作」や「振る舞い」をメソッドと呼ぶ
## 要するに何らかの処理をひとまとめにして名前を付け、何度も再利用できるようにしたもの
## メッセージと呼ばれることもある

# 状態(ステート)
## オブジェクト毎に保持されるデータのことを「オブジェクトの状態（もしくはステート）」と呼ぶ
## 例えば「信号機オブジェクトの現在の状態は赤です」といった感じ
## Userクラスが持つ「名前」や「年齢」も「Userの状態」に含まれる

# 属性(アトリビュート、プロパティ)
## オブジェクトから取得（もしくはオブジェクトに設定）できる値のことを属性（もしくはアトリビュートやプロパティ）と呼ぶ